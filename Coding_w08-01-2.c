#include <stdio.h>

int main() {
    
    for ( int i = 2; i <= 10; i += 2) {  // กำหนดค่าเริ่มต้นเป็น 2, เงื่อนไข i ต้องไม่เกิน 10, เพิ่มค่า i ทีละ 2
        printf("%d ", i);   // แสดงผลลัพธ์
    } 
    printf("\nend of loop\n");      // แสดง end of loop เมื่อออกจากลูป

    return 0;
}
// การใช้ for loop แทน while loop ในโปรแกรมนี้ ทำให้โค้ดดูสั้นและอ่านง่ายขึ้น
// โดย for loop สามารถกำหนดค่าเริ้มต้น เงื่อนไข และการเพิ่มค่าตัวแปรในบรรทัดเดียวกันได้