#include <stdio.h>

int main() {
    int i = 2;    // กำหนดค่าเริ่มต้นเป็น 2

    while ( i <=10) {         // เงื่อนไขการทำงาน i ต้องไม่เกิน 10
        printf("%d ", i);     // แสดงผลลัพธ์
        i+=2;    // เพิ่มค่า i ทีละ 2
    }
    printf("\nend of loop\n");     // แสดงผลลัพธ์เมื่อออกจากลูป
    
    return 0;
}
// การใช้ while loop ในโปรแกรมนี้ การทำงานของลูปจะกำหนดค่าเริ่มต้น
// พิจารณาเงื่อนไข และเพิ่มค่าตัวแปรในบรรทัดแยกกัน